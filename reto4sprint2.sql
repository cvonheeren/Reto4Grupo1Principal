CREATE TABLE UBICACION
(
COD_UBICACION INT(3) PRIMARY KEY,
CIUDAD VARCHAR(20) NOT NULL,
PAIS VARCHAR(20)
);

CREATE TABLE ALOJAMIENTO
(
COD_ALOJAMIENTO INT(6) PRIMARY KEY,
COD_UBICACION INT(3),
LONGITUD FLOAT(6),
LATITUD FLOAT(6),
TIPO_ALOJAMIENTO CHAR,
ESTRELLAS INT(1),
ALTURA INT(2),
TARIFA_NORMAL FLOAT(6),
TARIFA_VERANO FLOAT(6),
REGARGO FLOAT(6),
DESAYUNO FLOAT(6),
DESCMPENSION FLOAT(6),
FOREIGN KEY (COD_UBICACION) REFERENCES UBICACION (COD_UBICACION)
);

CREATE TABLE CLIENTES
(
DNI VARCHAR(9) PRIMARY KEY,
CONTRASENA VARCHAR(20) NOT NULL,
NOMBRE VARCHAR(20),
APELLIDOS VARCHAR(20),
FECHANAC DATE
);

CREATE TABLE DORMITORIO
(
COD_DORMITORIO INT(6) PRIMARY KEY,
CTD_CAMASSIMPLES INT(3),
CTD_CAMASMATRIMONIO INT(2),
CTD_CAMASINFANTIL INT(2),
TAMANO INT(4) NOT NULL
);

CREATE TABLE ALOJAMIENTO_DORMITORIO
(
COD_ALOJAMIENTO INT(6),
COD_DORMITORIO INT(6),
CANTIDAD INT(3),
FOREIGN KEY (COD_ALOJAMIENTO) REFERENCES ALOJAMIENTO (COD_ALOJAMIENTO),
FOREIGN KEY (COD_DORMITORIO) REFERENCES DORMITORIO (COD_DORMITORIO)
);

CREATE TABLE RESERVAS
(
COD_RESERVA INT(6) PRIMARY KEY,
DNI VARCHAR(9),
COD_ALOJAMIENTO INT(6),
NUM_HABITACION INT(3),
FECHACOMPRA DATE,
FECHAENTRADA DATE,
FECHASALIDA DATE,
PRECIOTOTAL FLOAT(6),
FOREIGN KEY (COD_ALOJAMIENTO) REFERENCES ALOJAMIENTO (COD_ALOJAMIENTO),
FOREIGN KEY (DNI) REFERENCES CLIENTES (DNI)
);


